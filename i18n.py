def get_lang() -> str:
    return st.session_state.get("app_lang", "pt")

def set_lang(lang: str) -> None:
    st.session_state["app_lang"] = lang

def t(key: str, **kwargs) -> str:
    """Return translated string for key in current language. Falls back to key itself."""
    lang = get_lang()
    val = TRANSLATIONS.get(lang, TRANSLATIONS["pt"]).get(key)
    if val is None:
        # Try the other language
        val = TRANSLATIONS["pt"].get(key, key)
    if kwargs:
        try:
            val = val.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return val

